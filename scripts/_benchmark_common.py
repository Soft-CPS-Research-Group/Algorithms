# ---------------------------------------------------------------------------
# Random comparator (in-module so every benchmark uses the same RNG semantics)
# ---------------------------------------------------------------------------


class RandomAgent(BaseAgent):
    """Uniform random sampler over each agent's action space.

    Lives in the shared module so BC and IQL benchmarks compare against
    identical random behaviour for the same seed.
    """

    def __init__(self, seed: int) -> None:
        super().__init__()
        self.use_raw_observations = True
        self._seed = int(seed)
        self._rng = np.random.default_rng(self._seed)
        self._lows: List[np.ndarray] = []
        self._highs: List[np.ndarray] = []

    def attach_environment(  # type: ignore[override]
        self,
        *,
        observation_names,
        action_names,
        action_space,
        observation_space,
        metadata=None,
    ) -> None:
        self._lows = [np.asarray(s.low, dtype=np.float32).flatten() for s in action_space]
        self._highs = [np.asarray(s.high, dtype=np.float32).flatten() for s in action_space]

    def predict(self, observations, deterministic: Optional[bool] = None):  # type: ignore[override]
        actions: List[List[float]] = []
        for low, high in zip(self._lows, self._highs):
            sample = self._rng.uniform(low=low, high=high).astype(np.float32)
            actions.append(sample.tolist())
        return actions

    def update(self, *args, **kwargs) -> None:  # type: ignore[override]
        return None

    def export_artifacts(  # type: ignore[override]
        self, output_dir, context=None
    ) -> Dict[str, Any]:
        return {"artifact_type": "random_baseline", "seed": self._seed}


# ---------------------------------------------------------------------------
# Env construction & rollout
# ---------------------------------------------------------------------------
#
# Flat-interface environment used for legacy BC/IQL benchmarks.
# Uses ``citylearn_three_phase_electrical_service_demo`` (static topology, 17
# buildings) so the schema does not declare dynamic topology — that would force
# ``interface='entity'`` and break flat-mode agents.  Under these flags:
#   * ``env.reset()`` / ``env.step()`` return per-agent lists.
#   * ``env.observation_names[i]`` are the 35-name flat per-building schema.
#   * ``env.action_space`` is a list of per-agent ``Box`` spaces.
#   * Topology stays static at 17 buildings, matching M2.


def make_env(seed: int) -> CityLearnEnv:
    return CityLearnEnv(
        schema=DATASET_SCHEMA,
        central_agent=False,
        interface="flat",
        topology_mode="static",
        reward_function=V2GPenaltyReward,
        random_seed=int(seed),
    )


def clip_actions(
    actions: List[List[float]], action_space: Sequence[Any]
) -> List[List[float]]:
    """Clip per-agent actions to their respective bounds."""
    clipped: List[List[float]] = []
    for vec, space in zip(actions, action_space):
        low = np.asarray(
            getattr(space, "low", np.full(len(vec), -np.inf)), dtype=np.float64
        ).flatten()
        high = np.asarray(
            getattr(space, "high", np.full(len(vec), np.inf)), dtype=np.float64
        ).flatten()
        arr = np.asarray(vec, dtype=np.float64).flatten()
        if arr.shape[0] != low.shape[0]:
            if arr.shape[0] < low.shape[0]:
                arr = np.concatenate([arr, np.zeros(low.shape[0] - arr.shape[0])])
            else:
                arr = arr[: low.shape[0]]
        clipped.append(np.clip(arr, low, high).tolist())
    return clipped


def extract_kpis(
    df: pd.DataFrame,
    *,
    level: str,
    name: Optional[str] = None,
) -> Dict[str, float]:
    sub = df[df["level"] == level]
    if name is not None:
        sub = sub[sub["name"] == name]
    out: Dict[str, float] = {}
    for _, row in sub.iterrows():
        try:
            out[str(row["cost_function"])] = float(row["value"])
        except (TypeError, ValueError):
            continue
    return out


def rollout(agent: BaseAgent, *, env_seed: int, label: str) -> Dict[str, Any]:
    """Run one full-episode rollout under a fresh env and return KPIs."""
    env = make_env(env_seed)
    obs_list, _ = env.reset()

    agent.attach_environment(
        observation_names=env.observation_names,
        action_names=env.action_names,
        action_space=env.action_space,
        observation_space=env.observation_space,
        metadata={
            "seconds_per_time_step": getattr(env, "seconds_per_time_step", None),
            "building_names": [b.name for b in env.buildings],
            "interface": "flat",
            "topology_mode": "static",
            "topology_version": 0,
        },
    )

    n_agents = len(env.action_names)
    terminated = False
    truncated = False
    step = 0

    while not (terminated or truncated):
        actions = agent.predict(obs_list, deterministic=True)
        if not isinstance(actions, list) or len(actions) != n_agents:
            raise RuntimeError(
                f"[{label}] agent.predict returned {type(actions).__name__} of "
                f"length {len(actions) if hasattr(actions,'__len__') else '?'}, "
                f"expected list of {n_agents} per-agent vectors."
            )
        actions = clip_actions(actions, env.action_space)
        obs_list, _rewards, terminated, truncated, _info = env.step(actions)
        step += 1
        if step % 1000 == 0:
            print(f"  [{label} seed={env_seed}] step {step}", flush=True)

    kpi_df = env.evaluate()
    print(f"  [{label} seed={env_seed}] episode done in {step} steps", flush=True)

    district = extract_kpis(kpi_df, level="district")
    building = extract_kpis(kpi_df, level="building", name=TARGET_BUILDING_NAME)
    return {
        "env_seed": env_seed,
        "kpi_df": kpi_df,
        "district": district,
        "building": building,
        "steps": step,
    }


# ---------------------------------------------------------------------------
# Comparator rollouts (RBC, Random) — used by every algo benchmark
# ---------------------------------------------------------------------------


def run_rbc_rollouts(env_seeds: Sequence[int]) -> List[Dict[str, Any]]:
    runs: List[Dict[str, Any]] = []
    for env_seed in env_seeds:
        agent = RuleBasedPolicy(config={
            "algorithm": {"hyperparameters": {}},
            "simulator": {"dataset_path": DATASET_SCHEMA},
        })
        runs.append(rollout(agent, env_seed=env_seed, label="RBC"))
    return runs


def run_random_rollouts(env_seeds: Sequence[int]) -> List[Dict[str, Any]]:
    runs: List[Dict[str, Any]] = []
    for env_seed in env_seeds:
        agent = RandomAgent(seed=int(env_seed))
        runs.append(rollout(agent, env_seed=env_seed, label="Random"))
    return runs


def discover_seed_checkpoints(root: Path) -> List[Tuple[int, Path, Path]]:
    """Return ``(seed, model_path, stats_path)`` tuples sorted by seed.

    Expects ``<root>/seed_<N>/model.pth`` + ``normalization_stats.json``.
    Used by both BC and IQL benchmarks (the on-disk layout is identical).
    """
    found: List[Tuple[int, Path, Path]] = []
    for entry in sorted(root.iterdir() if root.is_dir() else []):
        if not entry.is_dir() or not entry.name.startswith("seed_"):
            continue
        try:
            seed = int(entry.name.split("_", 1)[1])
        except ValueError:
            continue
        model = entry / "model.pth"
        stats = entry / "normalization_stats.json"
        if model.is_file() and stats.is_file():
            found.append((seed, model, stats))
    if not found:
        raise FileNotFoundError(
            f"No seed dirs with model.pth+normalization_stats.json under {root}"
        )
    return found


# ---------------------------------------------------------------------------
# Aggregation
# ---------------------------------------------------------------------------


def mean_std(values: Sequence[float]) -> Tuple[Optional[float], Optional[float]]:
    clean = [
        v for v in values
        if v is not None and not (isinstance(v, float) and np.isnan(v))
    ]
    if not clean:
        return None, None
    if len(clean) == 1:
        return float(clean[0]), 0.0
    return float(statistics.mean(clean)), float(statistics.stdev(clean))


def aggregate(
    runs: Sequence[Dict[str, Any]], *, scope: str, kpi_keys: Sequence[str]
) -> Dict[str, Dict[str, Optional[float]]]:
    """Return ``{kpi: {'mean': ..., 'std': ..., 'n': ...}}``."""
    out: Dict[str, Dict[str, Optional[float]]] = {}
    for kpi in kpi_keys:
        vals = [r[scope].get(kpi) for r in runs if scope in r]
        m, s = mean_std(vals)
        out[kpi] = {
            "mean": m,
            "std": s,
            "n": int(sum(
                1 for v in vals
                if v is not None and not (isinstance(v, float) and np.isnan(v))
            )),
        }
    return out


def union_keys(runs: Sequence[Dict[str, Any]], scope: str) -> List[str]:
    return sorted(set().union(*(r[scope].keys() for r in runs)))


# ---------------------------------------------------------------------------
# Markdown formatting helpers
# ---------------------------------------------------------------------------


def fmt(value: Optional[float], *, digits: int = 4) -> str:
    if value is None:
        return "—"
    if isinstance(value, float) and np.isnan(value):
        return "—"
    return f"{value:.{digits}f}"


def fmt_mean_std(stat: Dict[str, Optional[float]], *, digits: int = 4) -> str:
    m = stat.get("mean")
    s = stat.get("std")
    if m is None:
        return "—"
    return f"{fmt(m, digits=digits)} ± {fmt(s, digits=digits)}"


def delta_significance(
    cand: Dict[str, Optional[float]],
    base: Dict[str, Optional[float]],
    *,
    cand_label: str = "cand",
    base_label: str = "base",
    abs_floor: float = ABS_FLOOR_DEFAULT,
) -> Tuple[Optional[float], str]:
    """Heuristic verdict for ``cand`` vs ``base`` (lower-is-better KPIs).

    Returns ``(delta, verdict)`` where ``delta = cand_mean - base_mean``.
    Verdict:
      * ``≈ within noise`` if ``|Δ| ≤ max(cand_std, base_std, abs_floor)``
      * ``🟢 {cand_label} better`` if Δ < 0 by more than the threshold
      * ``🔴 {base_label} better`` if Δ > 0 by more than the threshold

    The ``abs_floor`` (default 1e-4) prevents the verdict from flipping
    based on sub-rounding-noise differences when both controllers are
    bit-for-bit identical (e.g. BC perfectly imitating a deterministic
    RBC produces ``std ~ 1e-14`` and ``Δ ~ 1e-15``).
    """
    cm, cs = cand.get("mean"), cand.get("std")
    bm, bs = base.get("mean"), base.get("std")
    if cm is None or bm is None:
        return None, "—"
    delta = cm - bm
    threshold = max(cs or 0.0, bs or 0.0, abs_floor)
    if abs(delta) <= threshold:
        marker = "≈ within noise"
    elif delta < 0:
        marker = f"🟢 {cand_label} better"
    else:
        marker = f"🔴 {base_label} better"
    return delta, marker


def render_kpi_table_two(
    title: str,
    *,
    base_agg: Dict[str, Dict[str, Optional[float]]],
    cand_agg: Dict[str, Dict[str, Optional[float]]],
    extra_agg: Dict[str, Dict[str, Optional[float]]],
    keys: Sequence[str],
    base_label: str,
    cand_label: str,
    extra_label: str,
) -> str:
    """3-column comparator table (BC-style benchmarks).

    Columns: ``extra | base | cand | Δ(cand - base) | verdict``. Used by the
    M3 BC benchmark with extra=Random, base=RBC, cand=BC. Kept as a separate
    helper so the M4 4-way table can re-use the same row format.
    """
    lines = [
        f"### {title}",
        "",
        f"| KPI | {extra_label} (mean ± std) | {base_label} (mean ± std) | "
        f"{cand_label} (mean ± std) | Δ ({cand_label} − {base_label}) | Verdict |",
        "|---|---:|---:|---:|---:|:---|",
    ]
    for k in keys:
        ex = extra_agg.get(k, {"mean": None, "std": None})
        ba = base_agg.get(k, {"mean": None, "std": None})
        ca = cand_agg.get(k, {"mean": None, "std": None})
        if ex["mean"] is None and ba["mean"] is None and ca["mean"] is None:
            continue
        delta, verdict = delta_significance(
            ca, ba, cand_label=cand_label, base_label=base_label
        )
        lines.append(
            f"| `{k}` | {fmt_mean_std(ex)} | {fmt_mean_std(ba)} | "
            f"{fmt_mean_std(ca)} | {fmt(delta)} | {verdict} |"
        )
    return "\n".join(lines) + "\n"


def render_full_dump_two(
    *,
    base_agg: Dict[str, Dict[str, Optional[float]]],
    cand_agg: Dict[str, Dict[str, Optional[float]]],
    extra_agg: Dict[str, Dict[str, Optional[float]]],
    base_label: str,
    cand_label: str,
    extra_label: str,
) -> str:
    keys = sorted(set(base_agg) | set(cand_agg) | set(extra_agg))
    lines = [
        f"| KPI | {extra_label} | {base_label} | {cand_label} | "
        f"Δ ({cand_label} − {base_label}) |",
        "|---|---:|---:|---:|---:|",
    ]
    for k in keys:
        ex = extra_agg.get(k, {"mean": None, "std": None})
        ba = base_agg.get(k, {"mean": None, "std": None})
        ca = cand_agg.get(k, {"mean": None, "std": None})
        delta = (
            (ca["mean"] - ba["mean"])
            if (ca["mean"] is not None and ba["mean"] is not None)
            else None
        )
        lines.append(
            f"| `{k}` | {fmt_mean_std(ex)} | {fmt_mean_std(ba)} | "
            f"{fmt_mean_std(ca)} | {fmt(delta)} |"
        )
    return "\n".join(lines) + "\n"


def render_kpi_table_four(
    title: str,
    *,
    aggs: Dict[str, Dict[str, Dict[str, Optional[float]]]],
    keys: Sequence[str],
    column_order: Sequence[str],
    base_label: str,
    cand_label: str,
) -> str:
    """N-column comparator table for M4-style benchmarks (Random/RBC/BC/IQL).

    ``aggs`` maps controller label → KPI aggregate dict. ``column_order`` is
    the left-to-right order of columns. The verdict column is always
    ``cand_label`` vs ``base_label`` (e.g. IQL vs RBC).
    """
    header_cols = " | ".join(f"{lbl} (mean ± std)" for lbl in column_order)
    align_cols = "---:|" * len(column_order)
    lines = [
        f"### {title}",
        "",
        f"| KPI | {header_cols} | Δ ({cand_label} − {base_label}) | Verdict |",
        f"|---|{align_cols}---:|:---|",
    ]
    for k in keys:
        row_cells = []
        any_value = False
        for lbl in column_order:
            cell = aggs.get(lbl, {}).get(k, {"mean": None, "std": None})
            if cell["mean"] is not None:
                any_value = True
            row_cells.append(fmt_mean_std(cell))
        if not any_value:
            continue
        cand = aggs.get(cand_label, {}).get(k, {"mean": None, "std": None})
        base = aggs.get(base_label, {}).get(k, {"mean": None, "std": None})
        delta, verdict = delta_significance(
            cand, base, cand_label=cand_label, base_label=base_label
        )
        lines.append(
            f"| `{k}` | " + " | ".join(row_cells) + f" | {fmt(delta)} | {verdict} |"
        )
    return "\n".join(lines) + "\n"


def render_full_dump_four(
    *,
    aggs: Dict[str, Dict[str, Dict[str, Optional[float]]]],
    column_order: Sequence[str],
    base_label: str,
    cand_label: str,
) -> str:
    all_keys = sorted(set().union(*(aggs[lbl].keys() for lbl in column_order)))
    header_cols = " | ".join(column_order)
    align_cols = "---:|" * len(column_order)
    lines = [
        f"| KPI | {header_cols} | Δ ({cand_label} − {base_label}) |",
        f"|---|{align_cols}---:|",
    ]
    for k in all_keys:
        cells = [
            fmt_mean_std(aggs.get(lbl, {}).get(k, {"mean": None, "std": None}))
            for lbl in column_order
        ]
        cand = aggs.get(cand_label, {}).get(k, {"mean": None, "std": None})
        base = aggs.get(base_label, {}).get(k, {"mean": None, "std": None})
        delta = (
            (cand["mean"] - base["mean"])
            if (cand["mean"] is not None and base["mean"] is not None)
            else None
        )
        lines.append("| `" + k + "` | " + " | ".join(cells) + f" | {fmt(delta)} |")
    return "\n".join(lines) + "\n"

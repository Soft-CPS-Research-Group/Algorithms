"""Shared primitives for offline-RL benchmark scripts (M3 BC, M4 IQL, ...).

Contains everything that is **not specific to the algorithm under test**:

* CityLearn env construction in the M2-collection mode
  (``interface='flat', topology_mode='static'``).
* Per-controller rollout loop with action clipping.
* KPI extraction (``CityLearnEnv.evaluate()`` → district / building dicts).
* Multi-seed aggregation (mean ± std, NaN-safe).
* RBC and Random comparator rollouts (used by every algo benchmark).
* Markdown formatting helpers (cell formatting, verdict heuristic, KPI tables).
* Constants for headline KPI lists and the Building-5 target.

What lives in the per-algo script (``benchmark_bc_m3.py``,
``benchmark_iql_m4.py``):

* The agent factory (``OfflineBCAgent`` vs ``OfflineIQLAgent`` etc.).
* Discovery of training seed dirs (``seed_<N>/model.pth``).
* The report's narrative copy (title, explanatory tables, verdict heuristic
  prose).
* Argparse + main glue.

The verdict heuristic is intentionally re-exported here unchanged from M3 so
that the BC report regenerated by the new module produces **bit-identical**
verdict columns to the original M3 release.
"""

